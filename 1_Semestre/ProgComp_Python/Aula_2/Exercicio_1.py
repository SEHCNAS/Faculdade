# Exercicio de calculadora com dois nomes
Numero_1 = float(input('Digite o primeiro numero: '))
Numero_2 = float(input('Digite o segundo numero: '))

print(f'A soma é {Numero_1 + Numero_2}')
print(f'A sutração é {Numero_1 - Numero_2}')
print(f'A multiplicação é {Numero_1 * Numero_2}')
print(f'A divisão é {Numero_1 / Numero_2}')
print(f'O resto de divisão é {Numero_1 % Numero_2}')
